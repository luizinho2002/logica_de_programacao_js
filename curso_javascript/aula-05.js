// Declara a variável chamada idade que armazena a idade da pessoa
let idade = 1;

/* 
   Inicializa um loop while que verifica se a idade é menor que 18

   Se a idade for menor que 18, o bloco de código é executado
*/
while(idade < 18) {

     // O bloco de código imprime a mensagem 'PASSOU' e o valor da idade atual no console do navegador
    console.log('PASSOU', idade);

    // Em seguida o valor da idade é icrementado em 1
    idade = idade + 1;
}

