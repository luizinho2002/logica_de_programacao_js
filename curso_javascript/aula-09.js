// Declara uma função chamada soma com os parâmetros (item1, item2) 
function soma(item1, item2) {

    // Retorna a soma entre (item1, item2)
    return item1 + item2;
}

// Declara uma função chamada diminuir com os parâmetros (item1, item2) 
function diminuir(item1, item2) {

    // Retorna a diferença entre (item1, item2)
    return item1 - item2;
}

// Imprime os resultados da soma e subtração na tela
console.log('SOMA: ', soma(5, 6));
console.log('SOMA: ', soma(15, 15));
console.log('SUBTRAÇÃO: ', diminuir(30, 15));