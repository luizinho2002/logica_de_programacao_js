// Declara duas variáveis, uma do tipo string e outra do tipo number
const salario = '103';

// Converte o valor da variável string para número
const salarioNumero = Number(salario);

// Imprime o valor das duas variáveis no console
console.log('salario: ', salario);
console.log('salario numero: ', salarioNumero);
console.log('salario numero: ', salarioNumero.toFixed(2));

